unsigned int	ft_strlcpy(char *dest, char *src, unsigned int size)
{
	unsigned int	i;
	unsigned int	src_len;

	src_len = 0;
	i = 0;
	while (src[src_len] != '\0')
		src_len++;
	if (size == 0)
		return (src_len);
	while (src[i] != '\0')
	{
		if (size - 1 > i)
		{
			dest[i] = src[i];
		}
		i++;
	}
	dest[i] = '\0';
	return (src_len);
}
